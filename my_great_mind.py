# Enable logging to capture errors and other information
logging.basicConfig(format='%(asctime)s - %(name)s - %(levelname)s - %(message)s',
                    level=logging.INFO)
logger = logging.getLogger(__name__)

# Function to send quotes in order
async def send_quotes_in_order(context: CallbackContext):
    # Send each quote in the list every 10 seconds
    for quote in quotes:
        quote_text = f'"{quote["quote"]}"\n\n— {quote["author"]}'
        
        # Adding the channel username to the end of the message
        message_to_send = f"✨ پند امروز ✨\n\n{quote_text} 🌱\n\n{CHANNEL_USERNAME}"
        
        try:
            # Send the quote to the channel
            await context.bot.send_message(CHANNEL_USERNAME, message_to_send)
            logger.info(f"Sent quote: {message_to_send}")
        except Exception as e:
            logger.error(f"Failed to send message: {e}")
        
        # Wait for 10 seconds before sending the next quote
        await asyncio.sleep(10)

# Error handler to catch and log exceptions
def error(update: Update, context: CallbackContext):
    logger.warning(f"Update {update} caused error {context.error}")

# Create the application and add handlers
application = Application.builder().token(TOKEN).build()

# Add the error handler
application.add_error_handler(error)

# Schedule the send_quotes_in_order function to run every 10 seconds
async def main():
    # Adding the job to the JobQueue
    job_queue: JobQueue = application.job_queue
    #job_queue.run_repeating(send_quotes_in_order, interval=10, first=0)
    job_queue.run_repeating(send_quotes_in_order, interval=12 * 60 * 60, first=0)  # 12 hours in seconds
    # Start polling to receive updates
    await application.run_polling(timeout=10, poll_interval=5)

if __name__ == '__main__':
    try:
        logger.info("Bot started...")
        asyncio.run(main())  # Run the async main function
    except KeyboardInterrupt:
        logger.info("Bot stopped manually.")
    except Exception as e:
        logger.error(f"An error occurred: {e}")
